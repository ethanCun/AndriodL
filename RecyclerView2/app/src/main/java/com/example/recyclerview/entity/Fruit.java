package com.example.recyclerview.entity;

public class Fruit {

    private String img;
    private String name;

    public void setImg(String img) {
        this.img = img;
    }

    public String getImg() {
        return img;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }
}
